package io.hatchways.assessment.exception;

public class SortByNotFoundException extends RuntimeException{

public SortByNotFoundException() {
		
	}

	public SortByNotFoundException(String message) {
		super(message);
		
	}

	public SortByNotFoundException(Throwable cause) {
		super(cause);
		
	}

	public SortByNotFoundException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public SortByNotFoundException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	
	

}
